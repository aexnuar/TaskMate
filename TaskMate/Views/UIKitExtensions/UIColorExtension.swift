extension UIColor {
    static var customBlackForBackground: UIColor {
        return UIColor(red: 4/255, green: 4/255, blue: 4/255, alpha: 1)
    }
    
    static var customWhiteForFont: UIColor {
        UIColor(red: 244/255, green: 244/255, blue: 244/255, alpha: 1)
    }
    
    static var customGrayForSeparator: UIColor {
        UIColor(red: 77/255, green: 85/255, blue: 94/255, alpha: 1)
    }
    
    static var customGrayForSearch: UIColor {
        UIColor(red: 39/255, green: 39/255, blue: 41/255, alpha: 1)
    }
    
    static var customYellowForButton: UIColor {
        UIColor(red: 254/255, green: 215/255, blue: 2/255, alpha: 1)
    }
}
